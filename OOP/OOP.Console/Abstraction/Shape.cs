namespace OOP.Console.Abstraction;
/// <summary>
/// Abstraction is another fundamental concept in Object-Oriented Programming (OOP). It means hiding the complex
/// reality while exposing only the necessary parts. Abstraction allows you to focus on what an object does instead of
/// how it accomplishes it. Essentially, it allows you to hide the implementation details and only show the features
/// to the outside world.
/// </summary>
public abstract class Shape
{
    public abstract double Area();
}